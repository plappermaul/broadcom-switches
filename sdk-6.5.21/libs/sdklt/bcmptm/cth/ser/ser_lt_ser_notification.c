/* Sync cache data to IMM LT SER_NOTIFICATION */
static int
bcmptm_ser_notification_imm_update(int unit,
                                   bcmptm_ser_lt_flds_update_t *fld_info_array,
                                   uint32_t num)
{
    int rv = SHR_E_NONE;
    bcmltd_fields_t lt_flds;
    uint32_t i = 0;

    lt_flds.count = num;
    lt_flds.field = bcmptm_ser_imm_flds_allocate(unit, num);
    if (lt_flds.field == NULL) {
        return SHR_E_MEMORY;
    }

    for (i = 0; i < num; i++) {
        bcmptm_ser_imm_fld_set(lt_flds, i, fld_info_array[i].lt_fid,
                               0, fld_info_array[i].data);
    }
    rv = bcmimm_entry_update(unit, true, SER_NOTIFICATIONt, &lt_flds);

    bcmptm_ser_imm_flds_free(unit, lt_flds.field, num);

    return rv;
}

/******************************************************************************
 * Public Functions
 */
int
bcmptm_ser_hw_fault_notify(int unit, bcmdrd_sid_t sid,
                           bool hw_fault, bool high_severity_err)
{
    bcmptm_ser_lt_flds_update_t fld_info_array[3] = {
        { SER_NOTIFICATIONt_PT_IDf, 0 },
        { SER_NOTIFICATIONt_HW_FAULTf, 0 },
        { SER_NOTIFICATIONt_HIGH_SEVERITY_ERRf, 0 }
    };
    uint32_t fld_num = COUNTOF(fld_info_array);

    if (hw_fault) {
        (void)bcmptm_ser_pt_status_imm_update
            (unit, sid, SER_PT_STATUSt_HW_FAULTf, 1);
    }
    if (high_severity_err) {
        (void)bcmptm_ser_pt_status_imm_update
            (unit, sid, SER_PT_STATUSt_HIGH_SEVERITY_ERRf, 1);
    }
    fld_info_array[0].data = sid;
    fld_info_array[1].data = hw_fault ? 1 : 0;
    fld_info_array[2].data = high_severity_err ? 1 : 0;

    return bcmptm_ser_notification_imm_update(unit, fld_info_array, fld_num);
}

int
bcmptm_ser_notification_init(int unit)
{
    shr_fmm_t **fld_array = NULL;
    bcmltd_fields_t lt_flds;
    uint32_t count = 0, lt_fid, field_data;
    uint32_t fld_num = SER_NOTIFICATIONt_FIELD_COUNT;
    int rv;
    bcmlrd_client_field_info_t *fields_info = NULL;
    size_t j, actual_fld_num, field_info_size;

    SHR_FUNC_ENTER(unit);

    field_info_size = sizeof(bcmlrd_client_field_info_t) * fld_num;
    fields_info = sal_alloc(field_info_size , "bcmptmSerNotifyFldInfo");
    SHR_NULL_CHECK(fields_info, SHR_E_MEMORY);

    sal_memset(fields_info, 0, field_info_size);
    rv = bcmlrd_table_fields_def_get(unit, "SER_NOTIFICATION", (size_t)fld_num,
                                     fields_info, &actual_fld_num);
    SHR_IF_ERR_EXIT(rv);

    fld_array = bcmptm_ser_imm_flds_allocate(unit, fld_num);
    SHR_NULL_CHECK(fields_info, SHR_E_MEMORY);

    lt_flds.field = fld_array;
    for (lt_fid = 0; lt_fid < fld_num; lt_fid++) {
        /* Set field value as default value */
        for (j = 0; j < actual_fld_num; j++) {
            if (fields_info[j].id == lt_fid) {
                break;
            }
        }
        if (j == actual_fld_num) {
            continue;
        }
        BCMPTM_SER_FLD_DEF_VAL_GET(fields_info[j], field_data);
        bcmptm_ser_imm_fld_set(lt_flds, count, lt_fid, 0, field_data);
        /* Get value of "INVALID_PT" generated by LT */
        if (lt_fid == SER_NOTIFICATIONt_PT_IDf) {
            bcmptm_ser_log_invalid_pt_id_set(unit, field_data);
        }
        count++;
    }
    lt_flds.count = count;
    rv = bcmimm_entry_insert(unit, SER_NOTIFICATIONt, &lt_flds);
    SHR_IF_ERR_EXIT(rv);

exit:
    if (fld_array) {
        bcmptm_ser_imm_flds_free(unit, fld_array, fld_num);
    }
    if (fields_info) {
        sal_free(fields_info);
    }
    if (SHR_FUNC_ERR()) {
        LOG_ERROR(BSL_LOG_MODULE,
                  (BSL_META_U(unit,
                              "Fail to commit data to SER_NOTIFICATIONt\n")));
    }
    SHR_FUNC_EXIT();
}
