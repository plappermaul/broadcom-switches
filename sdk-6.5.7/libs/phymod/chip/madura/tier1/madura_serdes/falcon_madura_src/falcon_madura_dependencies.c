uint8_t falcon_furia_madura_get_lane(const phymod_access_t *pa) {
    uint8_t i = 0 , lane = -1 ; 
    uint32_t reg_val,val;
    
    READ_MADURA_PMA_PMD_REG(pa, DEV1_SLICE_SLICE_ADR, reg_val);
    val = reg_val;
    val = val & 0xF; /*lane from 0-3*/
    for(i=0;i<4;i++)
    {
         if((1<<i)& val)/*considering that only one lane is selected*/
	 {
	      lane=i;
  	      break;
	 }
    } 
    if( reg_val & 0x200)
    { 
	lane += 4; /*upper slice*/	
    }
    /*else if( reg_val & 0x200)
    {
 	lower slice . Do nothing
    } */
    return (lane);
}
