static const uint16_t M0_DATA_RAM_LENGTH    = 0x1400;     /* fixed by hardware design */

err_code_t falcon_furia_madura_pmd_rdt_reg(const phymod_access_t *pa, uint16_t address, uint16_t *val) {
    uint32_t data = 0;

    READ_MADURA_PMA_PMD_REG(pa, (uint32_t)address, data);
    *val = (uint16_t)data;
    return ( ERR_CODE_NONE );
}

err_code_t falcon_furia_madura_pmd_wr_reg(const phymod_access_t *pa, uint16_t address, uint16_t val) {
    uint32_t data = 0xffff & val;

    WRITE_MADURA_PMA_PMD_REG(pa, (uint32_t) address, data);
    return(ERR_CODE_NONE);
}

err_code_t falcon_furia_madura_pmd_mwr_reg(const phymod_access_t *pa, uint16_t addr, uint16_t mask, uint8_t lsb, uint16_t val) {
    uint16_t tmp, otmp;
    uint32_t reg_val;
    val = val << lsb;
    
    val = val & mask ;
    /* Use clause 45 access if supported */
    READ_MADURA_PMA_PMD_REG(pa, addr, reg_val);
    tmp = (uint16_t) (reg_val & 0xffff);
    otmp = tmp;
    tmp &= ~(mask);
    tmp |= val;

    if (otmp != tmp) {
        WRITE_MADURA_PMA_PMD_REG(pa, addr, tmp);
    }
    return(ERR_CODE_NONE);
}

