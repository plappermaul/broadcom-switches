shr_error_e
dnx_data_module_testing_init(
    int unit,
    dnx_data_module_t *module_data)
{
    SHR_FUNC_INIT_VARS(unit);

    /* Data Struct Init */
    module_data->name = "module_testing";
    module_data->nof_submodules = dnx_data_module_testing_submodule_nof;
    module_data->submodules = sal_alloc(sizeof(dnx_data_submodule_t) * module_data->nof_submodules, "_dnx_data module_testing submodules");
    if (module_data->submodules == NULL)
    {
        SHR_ERR_EXIT(_SHR_E_MEMORY, "failed to allocate dnx_data module_testing submodules\n");
    }
    sal_memset(module_data->submodules, 0, sizeof(dnx_data_submodule_t) * module_data->nof_submodules);

    /*
     * Init Submodule data
     */
    SHR_IF_ERR_EXIT(dnx_data_module_testing_unsupported_init(unit, &module_data->submodules[dnx_data_module_testing_submodule_unsupported]));
    SHR_IF_ERR_EXIT(dnx_data_module_testing_types_init(unit, &module_data->submodules[dnx_data_module_testing_submodule_types]));
    SHR_IF_ERR_EXIT(dnx_data_module_testing_property_methods_init(unit, &module_data->submodules[dnx_data_module_testing_submodule_property_methods]));
    SHR_IF_ERR_EXIT(dnx_data_module_testing_dbal_init(unit, &module_data->submodules[dnx_data_module_testing_submodule_dbal]));
    /*
     * Attach device module
     */
    if (SOC_IS_JERICHO_2_A0(unit))
    {
        SHR_IF_ERR_EXIT(jer2_a0_data_module_testing_attach(unit));
    }

exit:
    SHR_FUNC_EXIT;
}
