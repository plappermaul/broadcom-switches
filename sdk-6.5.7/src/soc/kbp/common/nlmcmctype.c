int NlmCm__tolower(int x)
{
    return (x >= 'A' && x <= 'Z') ? x - 'A' + 'a' : x;
}

int NlmCm__toupper(int x)
{
    return (x >= 'a' && x <= 'z') ? x - 'a' + 'A' : x;
}



