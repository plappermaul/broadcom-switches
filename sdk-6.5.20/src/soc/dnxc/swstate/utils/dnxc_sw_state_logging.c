void
dnx_sw_state_log_by_func_type_enable(
    int unit,
    uint32 f_type)
{
    sw_state_logging[unit]->logging_by_func_type[f_type] = TRUE;
}

void
dnx_sw_state_log_by_func_type_disable(
    int unit,
    uint32 f_type)
{
    sw_state_logging[unit]->logging_by_func_type[f_type] = FALSE;
}

void
dnx_sw_state_log_by_thread_enable(
    int unit,
    uint32 thread)
{
    sw_state_logging[unit]->logging_by_thread = thread;
}

void
dnx_sw_state_log_by_thread_disable(
    int unit,
    uint32 thread)
{
    
    if(sw_state_logging[unit]->logging_by_thread == thread)
    {
        sw_state_logging[unit]->logging_by_thread = DNX_SW_STATE_NONE_THREAD;
    }
}

uint8
sh_dnxc_swstate_logging_by_module_id_get(
    int unit,
    uint32 module_id)
{
    return sw_state_logging[unit]->logging_by_module_id[module_id];
}

uint8
sh_dnxc_swstate_logging_by_function_get(
    int unit,
    uint32 f_type)
{
    return sw_state_logging[unit]->logging_by_func_type[f_type];
}

uint8
sh_dnxc_swstate_logging_by_thread_get(
    int unit,
    uint32 thread)
{
    if (sw_state_logging[unit]->logging_by_thread == thread)
    {
        return TRUE;
    }

    return FALSE;
}

