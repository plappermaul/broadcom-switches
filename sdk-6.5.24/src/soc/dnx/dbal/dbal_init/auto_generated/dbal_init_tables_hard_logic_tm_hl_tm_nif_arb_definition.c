    SHR_FUNC_INIT_VARS(unit);
    SHR_IF_ERR_EXIT(_dbal_init_tables_hard_logic_tm_hl_tm_nif_arb_definition_nif_arb_mac_mode_config_init(unit, cur_table_param, table_info));
